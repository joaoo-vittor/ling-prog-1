/*

3.23 Imprimindo números por um loop. Escreva um
programa que utilize o looping para imprimir os núme-
ros de 1 a 10 lado a lado na mesma linha, com três espa-
ços entre os números.

*/

#include <stdio.h>

int main() {

  for (int i = 1; i <= 10; i++) {
    printf("%d ", i);
  }
  
  return 0;
}
